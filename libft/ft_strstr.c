#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int	i;

	if (*s2 == '\0')
		return ((char*)s1);
	i = 0;
	while (*s1)
	{
		if (*s1 == s2[i])
			i++;
		else
			i = 0;
		if (s2[i] == '\0')
			return ((char*)(s1 - i + 1));
		s1++;
	}
	return (NULL);
}
